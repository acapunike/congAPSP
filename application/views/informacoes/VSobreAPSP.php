            <b><font size="5">Histórico da Instituição</font></b>
          </center>
          <br>

          <p>&nbsp;&nbsp;&nbsp;<font size="3">A APSP foi fundada em 18 de agosto de 1972, por iniciativa de professores da Faculdade de Saúde Pública da USP e de sanitaristas da Secretaria de Estado da Saúde de São Paulo, com a intenção de encabeçar as discussões e projetos de saúde, a fim de concretizar as demandas sanitárias do Estado de São Paulo. O símbolo escolhido para a associação foi o de Hígia, deusa grega da saúde, da limpeza, do saneamento e da higiene, uma das três filhas de Asclépio, o deus grego da medicina. A primeira sede provisória da APSP foi a sala 13 do prédio da Faculdade de Saúde Pública, onde permaneceu por 13 anos. Sua primeira diretoria (biênio 1972 – 1973) foi presidida pelo idealizador da entidade, Dr. Rodolpho dos Santos Mascarenhas, tendo como vice-presidente o Dr. Roberto Brólio, ambos docentes da Faculdade de Saúde Pública. No ano seguinte, os dirigentes da associação criaram o primeiro estatuto, registrado em Cartório em 15 de agosto de 1973.
          </font></p>

          <p>&nbsp;&nbsp;&nbsp;<font size="3">                                        De acordo com ele, a APSP é uma entidade que representa as diversas categorias profissionais do campo da Saúde Pública, com as finalidades de promover o debate e posicionar-se sobre os problemas de saúde e a organização e funcionamento dos serviços; promover e estimular a formação e o aperfeiçoamento profissional no campo da Saúde Pública; congregar os profissionais do campo da Saúde Pública pela defesa de seus direitos e orientar do ponto de vista deontológico as atividades relacionadas com a área; e prestar assessoria dentro de suas finalidades. Outro propósito era difundir trabalhos técnicos e científicos aos seus associados, principalmente para aqueles que estavam afastados dos grandes centros e das bibliotecas especialistas, divulgando assim o conhecimento produzido na área da saúde. 
          </font></p>


           <p>&nbsp;&nbsp;&nbsp;<font size="3">          Outra preocupação dos diretores e associados era a de que a participação e a produção científica disponível se tornasse acessível aos profissionais interessados, assumindo valores simbólicos e oferecendo direito aos 3 números da Revista de Saúde Pública da Faculdade de Saúde Pública da USP, e desconto de 50% nos cursos oferecidos pela entidade. Os dois primeiros trabalhos distribuídos foram “Tópicos do Plano Decenal de Saúde para as Américas” da OPAS, e o Projeto de Lei do Poder Executivo federal a respeito do Sistema de Saúde e definições do campo de atividades dos respectivos ministérios. Com esta política de organização, no fim de 1973, a APSP já contava com quase 150 sócios, adotando então a estratégia de que cada sócio trouxesse mais dois profissionais, almejando-se que, desta forma, o quadro de associados aumentaria e se consolidaria, como de fato ocorre. Nesse ano a entidade comemora seus 35 anos e se move em direção à realização de 15º Congresso, contando pela primeira vez com a disseminação do evento em seus Núcleos Regionais, objeto da presente propositura.  

          </font></p>
          
